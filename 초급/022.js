// 비교 연산자
console.log(5 == 5);
console.log("5" == 5);
console.log(5 == "5");
console.log(5 != 1);
console.log(5 != "1");
console.log(5 != "5");
// 일치 연산자 : 자료형까지 일치하는지 확인
console.log(5 === 5);
console.log(5 === "5");
console.log(5 !== 10);
console.log(5 !== "5");

// 관계 연산자
console.log(5 > 3);
console.log(5 < 3);
console.log(5 <= 6);
console.log(5 >= 5);
